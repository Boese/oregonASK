package org.oregonask.entities;

public class DataTableInfo {
	public String model;
	public String first;
	public String second;
	public String third;
	
	public DataTableInfo() {}

	public String getModel() {
		return model;
	}

	public DataTableInfo setModel(String model) {
		this.model = model;
		return this;
	}

	public String getFirst() {
		return first;
	}

	public DataTableInfo setFirst(String first) {
		this.first = first;
		return this;
	}

	public String getSecond() {
		return second;
	}

	public DataTableInfo setSecond(String second) {
		this.second = second;
		return this;
	}

	public String getThird() {
		return third;
	}

	public DataTableInfo setThird(String third) {
		this.third = third;
		return this;
	}
}
